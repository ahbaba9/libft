#include <string.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*p;

	p = malloc(ft_strlen(s1) * sizeof(char) + 1);
	i = 0;
	if (p == 0)
		return (NULL);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}

